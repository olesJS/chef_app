    @Test
    @DisplayName("getConnection() перехоплює SQLException при некоректному URL та прокидає його далі")
    void testGetConnectionThrowsSQLExceptionOnInvalidUrl() {
        String originalUrl = System.getProperty("test.db.url");

        try {
            // Incorrect URL
            System.setProperty("test.db.url", "jdbc:invalid_subprotocol:mem:test_db");

            // Checking if getConnection() intercepts SQLException,
            assertThrows(java.sql.SQLException.class, () -> {
                DatabaseConnector.getConnection();
            }, "Метод повинен викинути SQLException при невалідному URL");

        } finally {
            if (originalUrl != null) {
                System.setProperty("test.db.url", originalUrl);
            } else {
                System.clearProperty("test.db.url");
            }
        }
    }


    @Test
    @DisplayName("save() перехоплює SQLException, робить rollback() та закриває з'єднання в finally (блок catch/finally)")
    void testSaveSaladThrowsSQLExceptionAndRollbacks() throws SQLException {
        Salad badSalad = new Salad("Збійний Салат");
        Product dummyProduct = new RootVegetable(1L, "Морква", 40.0, 1.0, 0.0, 9.0, 5.0);
        badSalad.addIngredient(new Ingredient(dummyProduct, 100, ProcessingState.RAW));

        try {
            // Deleting ingredients table
            try (Connection conn = DriverManager.getConnection(H2_URL, "sa", "");
                 Statement stmt = conn.createStatement()) {
                stmt.execute("DROP TABLE ingredients CASCADE");
            } catch (SQLException ignored) {}

            // Checking error in RuntimeException
            RuntimeException exception = assertThrows(RuntimeException.class, () -> saladRepository.save(badSalad));
            assertTrue(exception.getMessage().contains("Помилка БД при збереженні салату"));

        } finally {
            // Refresing DB structure
            try (Connection conn = DriverManager.getConnection(H2_URL, "sa", "");
                 Statement stmt = conn.createStatement()) {
                stmt.execute("CREATE TABLE ingredients (" +
                        "id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, " +
                        "salad_id BIGINT, " +
                        "product_id BIGINT, " +
                        "weight_grams DOUBLE, " +
                        "processing_state VARCHAR(50), " +
                        "FOREIGN KEY(salad_id) REFERENCES salads(id) ON DELETE CASCADE, " +
                        "FOREIGN KEY(product_id) REFERENCES products(id))");
            } catch (SQLException ignored) {}
        }
    }


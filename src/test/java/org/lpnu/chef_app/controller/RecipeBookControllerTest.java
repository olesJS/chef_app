    @Test
    @DisplayName("saveToFile() успішно форматує та записує рецепт салату зі специфічними порадами у файл")
    void testSaveToFileSuccessWorkflow() throws java.io.IOException {
        Salad exportSalad = new Salad("Вітамінний");
        exportSalad.setId(10L);

        Product carrot = new RootVegetable(1L, "Морква", 41.0, 1.0, 0.1, 9.5, 12.0);
        Ingredient ing = new Ingredient(carrot, 150.0, ProcessingState.RAW);
        exportSalad.addIngredient(ing);

        java.io.File tempFile = java.io.File.createTempFile("salad_recipe_test", ".txt");
        tempFile.deleteOnExit();

        interact(() -> {
            try {
                java.lang.reflect.Method method = RecipeBookController.class.getDeclaredMethod(
                        "saveToFile", Salad.class, java.io.File.class);
                method.setAccessible(true);

                Platform.runLater(() -> {
                    try {
                        method.invoke(controller, exportSalad, tempFile);
                    } catch (Exception ignored) {}
                });
            } catch (NoSuchMethodException e) {
                fail("Не знайдено метод saveToFile через рефлексію");
            }
        });
        WaitForAsyncUtils.waitForFxEvents();

        clickOn("OK");
        WaitForAsyncUtils.waitForFxEvents();

        String fileContent = java.nio.file.Files.readString(tempFile.toPath());
        assertTrue(fileContent.contains("Рецепт салату: \"Вітамінний\""));
        assertTrue(fileContent.contains("Інгредієнт"));
        assertTrue(fileContent.contains("Морква"));
        assertTrue(fileContent.contains("Загальна енергетична цінність"));
    }


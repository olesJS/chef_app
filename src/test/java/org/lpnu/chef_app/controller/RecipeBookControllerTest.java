    @Test
    @DisplayName("Вибір інгредієнта в таблиці деталей відображає його кулінарну пораду в tipTextArea")
    void testIngredientSelectionShowsCookingTip() {
        // Selecting salad, so table takes ingredients from the mock
        ListView<Salad> listView = lookup("#saladListView").queryListView();
        interact(() -> listView.getSelectionModel().select(0));
        WaitForAsyncUtils.waitForFxEvents();

        TableView<Ingredient> detailsTable = lookup("#detailsTable").queryTableView();
        TextArea tipTextArea = lookup("#tipTextArea").queryAs(TextArea.class);

        // No preparation tip
        interact(() -> detailsTable.getSelectionModel().select(0));
        WaitForAsyncUtils.waitForFxEvents();

        assertEquals("Курка: поради немає.", tipTextArea.getText());

        // Present preparation tip
        interact(() -> detailsTable.getSelectionModel().select(2));
        WaitForAsyncUtils.waitForFxEvents();

        assertTrue(tipTextArea.getText().startsWith("Морква екстра:"));
        assertFalse(tipTextArea.getText().contains("поради немає."));
    }

    @Test
    @DisplayName("Скидання виділення інгредієнта (newVal == null) повністю очищує текстове поле порад")
    void testIngredientSelectionResetClearsTipTextArea() {
        // Selecting salad and ingredient, so preparation tip loads
        ListView<Salad> listView = lookup("#saladListView").queryListView();
        interact(() -> listView.getSelectionModel().select(0));
        WaitForAsyncUtils.waitForFxEvents();

        TableView<Ingredient> detailsTable = lookup("#detailsTable").queryTableView();
        TextArea tipTextArea = lookup("#tipTextArea").queryAs(TextArea.class);

        interact(() -> detailsTable.getSelectionModel().select(0));
        WaitForAsyncUtils.waitForFxEvents();
        assertFalse(tipTextArea.getText().isEmpty(), "Текстова область спочатку повинна містити текст");

        // Clearing selection
        interact(() -> detailsTable.getSelectionModel().clearSelection());
        WaitForAsyncUtils.waitForFxEvents();

        // Preparation tip form must be empty
        assertTrue(tipTextArea.getText().isEmpty(), "Після скасування виділення поле порад має очиститися");
    }


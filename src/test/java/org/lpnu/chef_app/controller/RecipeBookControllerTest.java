    @Override
    public void start(Stage stage) throws Exception {
        Locale.setDefault(Locale.US); // Double format (0.0 instead of 0,0)

        String h2Url = System.getProperty("test.db.url");
        if (h2Url == null) {
            h2Url = "jdbc:h2:mem:salad_db;DB_CLOSE_DELAY=-1;MODE=PostgreSQL";
            System.setProperty("test.db.url", h2Url);
        }

        try (Connection conn = DriverManager.getConnection(h2Url, "sa", "");
             Statement stmt = conn.createStatement()) {
            stmt.execute("CREATE TABLE IF NOT EXISTS salads (id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, name VARCHAR(255) NOT NULL)");
            stmt.execute("CREATE TABLE IF NOT EXISTS products (id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, name VARCHAR(255), product_type VARCHAR(50), kcal_per_100g DOUBLE, proteins DOUBLE, fats DOUBLE, carbs DOUBLE)");
            stmt.execute("CREATE TABLE IF NOT EXISTS ingredients (id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, salad_id BIGINT, product_id BIGINT, weight_grams DOUBLE, processing_state VARCHAR(50), FOREIGN KEY(salad_id) REFERENCES salads(id) ON DELETE CASCADE, FOREIGN KEY(product_id) REFERENCES products(id))");
        } catch (Exception ignored) {}

        mockSaladRepo = Mockito.mock(SaladRepository.class);
        mockMainController = Mockito.mock(MainController.class);

        testSalad = new Salad("Цезар");
        testSalad.setId(101L);

        Product chicken = new RootVegetable(1L, "Курка", 120.0, 20.0, 4.0, 0.0, 0.0);
        Product saladLeaves = new RootVegetable(2L, "Салат", 15.0, 1.0, 0.0, 2.0, 0.0);
        Product carrotWithTip = new RootVegetable(3L, "Морква екстра", 41.0, 1.0, 0.1, 9.5, 12.0);

        testSalad.addIngredient(new Ingredient(chicken, 100.0, ProcessingState.RAW)); // 120 kcal
        testSalad.addIngredient(new Ingredient(saladLeaves, 200.0, ProcessingState.RAW)); // 30 kcal
        testSalad.addIngredient(new Ingredient(carrotWithTip, 100.0, ProcessingState.RAW));

        when(mockSaladRepo.findAll()).thenReturn(List.of(testSalad));

        FXMLLoader loader = new FXMLLoader(Thread.currentThread().getContextClassLoader().getResource("org/lpnu/chef_app/views/recipe-book-view.fxml"));
        Scene scene = new Scene(loader.load());

        controller = loader.getController();
        controller.setSaladRepository(mockSaladRepo);
        controller.setMainController(mockMainController);

        interact(() -> controller.refresh());

        stage.setScene(scene);
        stage.show();
    }

